
public class Perso {
	private int adresse;
	private int Force;
	private int resistance;
	
	
	public int getadresse() {
		return this.adresse;
	}
	public void setadresse(int add) {
		this.adresse= add;
	}
	public int getForce() {
		return this.Force;
	}
	public void setForce(int f) {
		this.Force = f;
	}
	public int getres() {
		return this.resistance;
	}
	public void setres(int res) {
		this.resistance = res;
	}
}
